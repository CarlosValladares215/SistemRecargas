    private void BTN_RegistrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_RegistrarClienteActionPerformed
         try {
            String cedula = TXT_Cedula.getText().trim();
            String nombres = TXT_Nombres.getText().trim();
            String apellidos = TXT_Apellidos.getText().trim();

            if (cedula.isEmpty() || nombres.isEmpty() || apellidos.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            LogCliente logCliente = new LogCliente();
            logCliente.registrarCliente(cedula, nombres, apellidos);

            JOptionPane.showMessageDialog(null, "Cliente registrado correctamente.", "Ã‰xito", JOptionPane.INFORMATION_MESSAGE);

            // Limpiar campos
            TXT_Cedula.setText("");
            TXT_Nombres.setText("");
            TXT_Apellidos.setText("");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BTN_RegistrarClienteActionPerformed

